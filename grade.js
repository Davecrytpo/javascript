function cal_1(){
    var english = document.getElementById("english").value;
    var first = document.getElementById("first").value;
    if (english >= 80 ||  english == 100) {
        document.getElementById("first").value = ' A1';
    } else if(english >= 70 ||  english == 79){
        document.getElementById("first").value = 'B2';
    } else if(english >= 60 || english == 69){
        document.getElementById("first").value = ' B3';
    } else if(english >= 50 || english == 59){
        document.getElementById("first").value = ' C3'
    } else if(english >= 40 || english == 49){
        document.getElementById("first").value = ' C'
    } else if(english == 0 || english >= 39) {
        document.getElementById("first").value = 'F9'
    }
}
